/**
 * Experience type enum — sub-categories for experience commerce listings.
 *
 * Covers the main tourism-activity categories available in the Litoral region
 * of Argentina (Concepcion del Uruguay and surroundings).
 *
 * - CAR_RENTAL: Vehicle rental service (cars, vans).
 * - BIKE_RENTAL: Bicycle rental service.
 * - KAYAK_RENTAL: Kayak or canoe rental service.
 * - QUAD_RENTAL: ATV / quad-bike rental service.
 * - TOUR_GUIDE: Professional guided tourism services.
 * - GUIDED_VISIT: Structured guided visit to a specific site.
 * - EXCURSION: Half-day or full-day excursion to a destination.
 * - BOAT_TRIP: Boat ride or river cruise.
 * - FISHING_CHARTER: Recreational or sport fishing charter.
 * - BIRD_WATCHING: Guided bird-watching tour.
 * - CULTURAL_TOUR: Cultural heritage or historical tour.
 * - WINE_TASTING: Winery visit or wine-tasting experience.
 * - OUTDOOR_ADVENTURE: Outdoor adventure activity (hiking, rock climbing, etc.).
 * - OTHER: Catch-all for types not covered above.
 */
export enum ExperienceTypeEnum {
    CAR_RENTAL = 'CAR_RENTAL',
    BIKE_RENTAL = 'BIKE_RENTAL',
    KAYAK_RENTAL = 'KAYAK_RENTAL',
    QUAD_RENTAL = 'QUAD_RENTAL',
    TOUR_GUIDE = 'TOUR_GUIDE',
    GUIDED_VISIT = 'GUIDED_VISIT',
    EXCURSION = 'EXCURSION',
    BOAT_TRIP = 'BOAT_TRIP',
    FISHING_CHARTER = 'FISHING_CHARTER',
    BIRD_WATCHING = 'BIRD_WATCHING',
    CULTURAL_TOUR = 'CULTURAL_TOUR',
    WINE_TASTING = 'WINE_TASTING',
    OUTDOOR_ADVENTURE = 'OUTDOOR_ADVENTURE',
    OTHER = 'OTHER'
}
