/**
 * Request schema for the `moderate` capability (Q4, §5).
 *
 * Intentionally does NOT extend `AiCapabilityRequestBaseSchema` — moderation
 * is a lower-level utility that operates on raw text and does not require
 * feature routing via `AiFeatureSchema`. The engine hardwires the moderation
 * provider (typically the same as the primary text provider).
 *
 * `locale` is optional: content moderators may use it to apply locale-aware
 * policies, but the majority of providers treat moderation language-agnostically.
 */
export const ModerateRequestSchema = z
    .object({
        /** The text input to evaluate for policy violations. */
        input: z.string().min(1),
        /**
         * Optional locale hint for the input.
         * Providers that support locale-aware policies may use this to adjust
         * thresholds or categories.
         */
        locale: LanguageEnumSchema.optional()
    })
    .strict();

/** TypeScript type for a `moderate` request. */
export type ModerateRequest = z.infer<typeof ModerateRequestSchema>;

/**
 * Response schema for the `moderate` capability.
 *
 * Provider-agnostic shape. The engine normalises each provider's output into
 * this envelope so callers are insulated from provider differences.
 *
 * - `flagged`     — top-level flag: `true` if ANY category was violated.
 * - `categories`  — per-category boolean violation flags (open record).
 * - `scores`      — optional per-category confidence scores [0, 1] (open record).
 *                   Not all providers return scores; absent scores mean unknown.
 *
 * **Strictness**: NOT `.strict()` — providers may include additional diagnostic
 * fields that callers can safely ignore.
 */
export const ModerateResponseSchema = z.object({
    /**
     * `true` if the moderation result flagged any policy violation.
     * The caller should treat this as the primary decision signal.
     */
    flagged: z.boolean(),
    /**
     * Per-category violation flags.
     * Keys are provider-defined category names (e.g. `'hate'`, `'violence'`).
     * `true` means the category was flagged.
     */
    categories: z.record(z.string(), z.boolean()),
    /**
     * Optional per-category confidence scores in [0, 1].
     * Higher values indicate stronger confidence in the flag.
     * Absent when the provider does not return numeric scores.
     */
    scores: z.record(z.string(), z.number().min(0).max(1)).optional()
});

/** TypeScript type for a `moderate` response. */
export type ModerateResponse = z.infer<typeof ModerateResponseSchema>;
