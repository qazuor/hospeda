import { z } from 'zod';
import { LanguageEnumSchema } from '../user/user.settings.schema.js';
import { AiFeatureSchema, AiModelParamsSchema, AiProviderIdSchema } from './ai-provider.schema.js';

/**
 * AI capability request/response schemas (SPEC-173).
 *
 * Models the I/O contract for every engine capability exposed in §5 of the
 * spec. Each capability has a typed request schema and a typed response schema.
 * The engine uses these at every boundary: HTTP route validation, service-layer
 * calls, and (where relevant) streaming chunk shapes.
 *
 * **Locale reuse**: `LanguageEnumSchema` from `@repo/schemas` (SSoT:
 * `src/entities/user/user.settings.schema.ts`) is the platform-wide locale
 * discriminator (`'es' | 'en' | 'pt'`). It is imported here rather than
 * re-declared to keep the single source of truth.
 *
 * **Capabilities covered**:
 * - `generateText`  — single-shot or multi-turn text generation.
 * - `streamText`    — streaming text generation (chunk + final-meta shapes).
 * - `generateObject`— structured-object generation (envelope only; the target
 *                      Zod schema is a TYPE PARAM at the engine layer).
 * - `moderate`      — provider-agnostic content moderation (Q4, §5).
 *
 * **Shared building blocks**:
 * - `AiUsageStatsSchema` — token-count triple, reused across all responses.
 * - `AiMessageSchema` — single AI SDK v6 CoreMessage (role + content string).
 * - `AiCapabilityRequestBaseSchema` — feature, locale, optional model override,
 *                                      optional inference params.
 *
 * **Strictness**: request schemas use `.strict()` so that unrecognised keys are
 * rejected at the boundary. Response schemas are intentionally NOT `.strict()` —
 * provider adapters may attach diagnostic fields that the caller can ignore.
 */

// ---------------------------------------------------------------------------
// Shared: usage stats
// ---------------------------------------------------------------------------

/**
 * Token-usage statistics returned by every generate/stream response.
 *
 * All counts are non-negative integers (0 is valid when a provider does not
 * report a sub-count, e.g. some streaming adapters only fill `totalTokens`).
 */
export const AiUsageStatsSchema = z.object({
    /** Number of tokens in the prompt / input. */
    promptTokens: z.number().int().min(0),
    /** Number of tokens generated by the model. */
    completionTokens: z.number().int().min(0),
    /** Total tokens consumed (`promptTokens + completionTokens` in most providers). */
    totalTokens: z.number().int().min(0)
});

/** TypeScript type for AI token-usage statistics. */
export type AiUsageStats = z.infer<typeof AiUsageStatsSchema>;

// ---------------------------------------------------------------------------
// Shared: request base
// ---------------------------------------------------------------------------

/**
 * Common fields shared by every capability request.
 *
 * - `feature`  — routes the call to the correct engine config entry.
 * - `locale`   — the user's locale; forwarded to the system-prompt resolver so
 *                the engine can select the right localized prompt (§5.4).
 * - `model`    — optional override of the admin-configured model string for
 *                this call. When absent, the engine uses the feature config.
 * - `params`   — optional inference-time parameter overrides. When absent, the
 *                engine uses the feature config params.
 */
export const AiCapabilityRequestBaseSchema = z
    .object({
        /** AI feature routing key. Determines which config entry the engine uses. */
        feature: AiFeatureSchema,
        /**
         * User locale for prompt localization.
         * One of `'es'`, `'en'`, or `'pt'` (platform SSoT: `LanguageEnumSchema`).
         */
        locale: LanguageEnumSchema,
        /**
         * Optional model identifier override for this call.
         * Overrides the model set in `ai_settings` for the feature.
         * Concrete string values (e.g. `"gpt-4o-mini"`) are NOT validated here —
         * the provider adapter enforces them at call time.
         */
        model: z.string().min(1).optional(),
        /**
         * Optional inference-parameter overrides for this call.
         * Any field omitted here falls back to the feature config value.
         */
        params: AiModelParamsSchema.optional()
    })
    .strict();

/** TypeScript type for the shared capability request base. */
export type AiCapabilityRequestBase = z.infer<typeof AiCapabilityRequestBaseSchema>;

// ---------------------------------------------------------------------------
// Multi-turn message (AI SDK v6 CoreMessage, string-content V1 subset)
// ---------------------------------------------------------------------------

/**
 * A single message in a multi-turn AI conversation.
 *
 * Aligned with the Vercel AI SDK v6 `CoreMessage` shape (role + content).
 * V1 restricts `content` to a plain string — the AI SDK also supports
 * `ContentPart[]` arrays for image/tool content, but that is out of scope
 * for the V1 surface and can be added additively by a child spec.
 *
 * Roles follow the AI SDK convention:
 * - `system`    — System prompt or instructions prepended before the conversation.
 * - `user`      — User turn (human input).
 * - `assistant` — Model turn (prior assistant response for multi-turn context).
 *
 * **Decision (owner-approved 2026-06-04)**: V1 restricts `content` to a plain
 * `string`. The AI SDK v6 additionally supports `ContentPart[]` arrays (for
 * images, tool calls, etc.). Those are omitted (YAGNI); a child spec may extend
 * this schema additively if richer content types are needed.
 */
export const AiMessageSchema = z.object({
    /**
     * The role of the message author.
     * Must be one of `'system'`, `'user'`, or `'assistant'`.
     */
    role: z.enum(['system', 'user', 'assistant']),
    /**
     * The text content of the message.
     * Must be a non-empty string in V1. Empty messages are rejected to prevent
     * accidental blank turns that confuse the model.
     */
    content: z.string().min(1)
});

/** TypeScript type for a single AI conversation message. */
export type AiMessage = z.infer<typeof AiMessageSchema>;

// ---------------------------------------------------------------------------
// generateText
// ---------------------------------------------------------------------------

/**
 * Request schema for the `generateText` capability.
 *
 * Accepts EITHER a `prompt: string` (single-shot) OR a `messages: AiMessage[]`
 * array (multi-turn). Exactly one of the two must be provided — both together
 * and neither are rejected by the `.superRefine()` guard.
 *
 * **Decision (owner-approved 2026-06-04)**: support BOTH `prompt` and `messages`
 * from V1. The Vercel AI SDK v6 `generateText()` already accepts either form;
 * keeping the schema aligned with the SDK means no engine-layer translation cost.
 * `prompt` is sufficient for single-shot call sites (text_improve, search); a
 * `messages` array is needed for multi-turn chat and system+user composition.
 * The two are mutually exclusive to avoid ambiguity: if both are provided the
 * engine would have to pick one, so the schema rejects the combination at the
 * boundary. `generateObject` keeps `prompt`-only (structured output is one-shot
 * in V1 — see its own decision note).
 */
export const GenerateTextRequestSchema = AiCapabilityRequestBaseSchema.extend({
    /**
     * Plain-string prompt for single-shot generation.
     * Mutually exclusive with `messages`. Provide exactly one.
     */
    prompt: z.string().min(1).optional(),
    /**
     * Ordered message array for multi-turn / system+user composition.
     * Mutually exclusive with `prompt`. Provide exactly one.
     * Must contain at least one message.
     */
    messages: z.array(AiMessageSchema).min(1).optional()
})
    .strict()
    .superRefine((val, ctx) => {
        const hasPrompt = val.prompt !== undefined;
        const hasMessages = val.messages !== undefined;
        if (!hasPrompt && !hasMessages) {
            ctx.addIssue({
                code: z.ZodIssueCode.custom,
                message: 'Exactly one of "prompt" or "messages" must be provided (neither was).',
                path: ['prompt']
            });
        }
        if (hasPrompt && hasMessages) {
            ctx.addIssue({
                code: z.ZodIssueCode.custom,
                message:
                    'Exactly one of "prompt" or "messages" must be provided (both were given).',
                path: ['prompt']
            });
        }
    });

/** TypeScript type for a `generateText` request. */
export type GenerateTextRequest = z.infer<typeof GenerateTextRequestSchema>;

/**
 * Response schema for the `generateText` capability.
 *
 * Reflects the fields returned by the Vercel AI SDK v6 `generateText()` result
 * object: `text`, usage statistics, the resolved provider and model, and the
 * finish reason.
 */
export const GenerateTextResponseSchema = z.object({
    /** The generated text output from the model. */
    text: z.string(),
    /** Token usage for this call. */
    usage: AiUsageStatsSchema,
    /** The provider that ultimately served this response (after fallback resolution). */
    provider: AiProviderIdSchema,
    /**
     * The model identifier that was used, as reported by or passed to the adapter.
     * May differ from the requested model if the adapter normalised it.
     */
    model: z.string().min(1),
    /**
     * The finish reason reported by the model.
     * Common values: `'stop'` (natural end), `'length'` (max-tokens hit),
     * `'content-filter'` (moderation stop). The set is open — providers may
     * return additional values; callers should handle unknown strings gracefully.
     */
    finishReason: z.string().min(1)
});

/** TypeScript type for a `generateText` response. */
export type GenerateTextResponse = z.infer<typeof GenerateTextResponseSchema>;

