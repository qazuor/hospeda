/**
 * Request schema for the `streamText` capability.
 *
 * Identical prompt-or-messages contract to `GenerateTextRequestSchema` — the
 * capability is differentiated at the engine layer (streaming vs buffered
 * output), not at the request schema level.
 *
 * **Decision (owner-approved 2026-06-04)**: same as `generateText` — both
 * `prompt` and `messages` are supported from V1, mutually exclusive. See the
 * `GenerateTextRequestSchema` decision note for full rationale.
 */
export const StreamTextRequestSchema = AiCapabilityRequestBaseSchema.extend({
    /**
     * Plain-string prompt for single-shot streaming.
     * Mutually exclusive with `messages`. Provide exactly one.
     */
    prompt: z.string().min(1).optional(),
    /**
     * Ordered message array for multi-turn streaming.
     * Mutually exclusive with `prompt`. Provide exactly one.
     * Must contain at least one message.
     */
    messages: z.array(AiMessageSchema).min(1).optional()
})
    .strict()
    .superRefine((val, ctx) => {
        const hasPrompt = val.prompt !== undefined;
        const hasMessages = val.messages !== undefined;
        if (!hasPrompt && !hasMessages) {
            ctx.addIssue({
                code: z.ZodIssueCode.custom,
                message: 'Exactly one of "prompt" or "messages" must be provided (neither was).',
                path: ['prompt']
            });
        }
        if (hasPrompt && hasMessages) {
            ctx.addIssue({
                code: z.ZodIssueCode.custom,
                message:
                    'Exactly one of "prompt" or "messages" must be provided (both were given).',
                path: ['prompt']
            });
        }
    });

/** TypeScript type for a `streamText` request. */
export type StreamTextRequest = z.infer<typeof StreamTextRequestSchema>;

/**
 * Schema for a single delta chunk emitted during a `streamText` response.
 *
 * The SSE framing (event type, `data:` prefix, flush strategy) is handled by
 * the HTTP route layer (T-024), not here. This schema models only the payload
 * of a single streamed token delta.
 */
export const StreamTextChunkSchema = z.object({
    /**
     * Incremental text delta for this chunk.
     * May be an empty string for keep-alive / heartbeat frames (provider
     * dependent). Callers should concatenate non-empty deltas.
     */
    delta: z.string()
});

/** TypeScript type for a `streamText` delta chunk. */
export type StreamTextChunk = z.infer<typeof StreamTextChunkSchema>;

/**
 * Schema for the final metadata frame emitted at the end of a `streamText`
 * response, after all delta chunks.
 *
 * The engine emits this as the last SSE event so callers can record usage.
 * `delta` is absent — callers distinguish this from chunk frames by the
 * presence of `usage` / `finishReason`.
 */
export const StreamTextFinalMetaSchema = z.object({
    /** Aggregate token usage for the full stream. */
    usage: AiUsageStatsSchema,
    /** The provider that served the stream (after fallback resolution). */
    provider: AiProviderIdSchema,
    /** The model identifier that was used for the stream. */
    model: z.string().min(1),
    /** The finish reason for the stream (same semantics as `generateText`). */
    finishReason: z.string().min(1)
});

/** TypeScript type for the `streamText` final metadata frame. */
export type StreamTextFinalMeta = z.infer<typeof StreamTextFinalMetaSchema>;

// ---------------------------------------------------------------------------
// generateObject
// ---------------------------------------------------------------------------

/**
 * Request schema for the `generateObject` capability.
 *
 * **Important design note**: the target structured-output schema (what the
 * model should produce) is a TYPE PARAMETER at the engine layer, not a fixed
 * Zod schema here. The caller passes the Zod schema at call-time to the
 * engine's `generateObject<T>()` method; this request schema only covers the
 * _envelope_ (base fields + prompt).
 *
 * This schema intentionally does NOT include a `schema` field for the target
 * object shape — embedding an arbitrary Zod schema as a serialised value would
 * require runtime schema-from-JSON reconstruction, which is out of scope for V1
 * and fragile in general.
 *
 * **Decision (owner-approved 2026-06-04)**: the `generateObject` response shape
 * is intentionally opaque at the `@repo/schemas` layer — only the meta fields
 * (`usage`, `provider`, `model`, `finishReason`) are defined in
 * `GenerateObjectResponseMetaSchema`. The typed `object: T` field is a generic
 * type parameter known only at the engine call-site; callers compose the full
 * response type by merging their target schema with the meta schema:
 * `z.object({ object: MyOutputSchema }).merge(GenerateObjectResponseMetaSchema)`.
 * This avoids runtime schema-from-JSON reconstruction and keeps the schemas
 * layer free of domain-specific output shapes. `messages` support for
 * `generateObject` is omitted in V1 (structured output is one-shot); a child
 * spec may add it via `.extend()` if multi-turn structured output is needed.
 */
export const GenerateObjectRequestSchema = AiCapabilityRequestBaseSchema.extend({
    /**
     * The prompt instructing the model what object to produce.
     * The engine combines this with the active system prompt and the caller's
     * Zod schema to drive structured output.
     */
    prompt: z.string().min(1)
}).strict();

/** TypeScript type for a `generateObject` request. */
export type GenerateObjectRequest = z.infer<typeof GenerateObjectRequestSchema>;

/**
 * Response metadata for the `generateObject` capability.
 *
 * Contains only the invariant meta fields (usage, provider, model,
 * finishReason). The `object` field — which holds the typed structured output
 * — is NOT defined here because its shape is a generic type parameter `T`
 * known only at the engine call-site.
 *
 * Callers compose the full response type by merging their target schema with
 * this meta schema:
 * ```ts
 * const FullSchema = z.object({ object: MyOutputSchema }).merge(GenerateObjectResponseMetaSchema);
 * ```
 */
export const GenerateObjectResponseMetaSchema = z.object({
    /** Token usage for this call. */
    usage: AiUsageStatsSchema,
    /** The provider that served the response (after fallback resolution). */
    provider: AiProviderIdSchema,
    /** The model identifier that was used. */
    model: z.string().min(1),
    /** The finish reason reported by the model. */
    finishReason: z.string().min(1)
});

/** TypeScript type for `generateObject` response metadata (without the typed object). */
export type GenerateObjectResponseMeta = z.infer<typeof GenerateObjectResponseMetaSchema>;

