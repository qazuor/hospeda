/**
 * Accommodation Import — Raw-to-Draft Mapper (SPEC-222)
 *
 * Converts a {@link RawExtraction} (the loose, pre-validation bag produced by
 * an adapter) into a fully type-safe {@link AccommodationImportDraft} by
 * coercing each candidate field's value and validating it against the
 * corresponding Zod schema shape.
 *
 * **Key contracts:**
 * - Fields that fail coercion or Zod validation are silently dropped — never
 *   thrown. The caller always receives a valid (possibly empty) draft.
 * - Reviews and ratings are never mapped. `RawExtraction` already excludes
 *   them; this file adds no rating-field handling (SPEC-222 hard rule).
 * - Confidence is assigned purely from source: use `CONFIDENCE_BY_SOURCE[source]`.
 *
 * @module services/accommodation-import/mapping
 */

import {
    type AccommodationImportDraft,
    AccommodationImportDraftSchema,
    type FieldSource
} from '@repo/schemas';

import type { RawCandidateField, RawExtraction } from './adapter.types.js';

// ---------------------------------------------------------------------------
// CONFIDENCE_BY_SOURCE
// ---------------------------------------------------------------------------

/**
 * Default confidence score (0–100) for a field value given its extraction
 * source.
 *
 * Bands are chosen to reflect data reliability:
 * - `official_api` (95) — Data came directly from an official platform API.
 *   Highest confidence: machine-readable, structured, authoritative.
 * - `jsonld` (90)       — Parsed from a structured JSON-LD block. Very
 *   reliable: schema.org vocabulary with typed fields.
 * - `opengraph` (70)    — Parsed from Open Graph meta tags. Good reliability;
 *   intended for sharing but may be truncated or marketing-optimised.
 * - `meta` (65)         — Other HTML `<meta>` tags (description, keywords).
 *   Slightly less reliable than OG tags; prone to keyword stuffing.
 * - `text` (50)         — Extracted by heuristic DOM / text parsing.
 *   Medium confidence: pattern-matching may mis-identify values.
 * - `ai` (30)           — Inferred or generated by an AI model.
 *   Lowest confidence: plausible but unverified; requires host review.
 *
 * Being a `Record<FieldSource, number>` this object is exhaustiveness-checked:
 * adding a new `FieldSource` value in the schema will cause a TypeScript error
 * here until the band is defined.
 */
export const CONFIDENCE_BY_SOURCE: Readonly<Record<FieldSource, number>> = {
    official_api: 95,
    jsonld: 90,
    opengraph: 70,
    meta: 65,
    text: 50,
    ai: 30
} as const;

// ---------------------------------------------------------------------------
// Internal coercion helpers
// ---------------------------------------------------------------------------

/**
 * Coerces an unknown value to a trimmed string, or returns `undefined` when
 * the value cannot be meaningfully converted to a non-empty string.
 *
 * Accepts strings (trimmed) and numbers/booleans (via String conversion).
 * Rejects `null`, `undefined`, objects, and arrays.
 *
 * @param v - The raw candidate value.
 * @returns A trimmed string, or `undefined` if coercion is not possible.
 */
function toTrimmedString(v: unknown): string | undefined {
    if (typeof v === 'string') {
        return v.trim();
    }
    if (typeof v === 'number' || typeof v === 'boolean') {
        return String(v);
    }
    return undefined;
}

/**
 * Coerces an unknown value to a non-negative integer, or returns `undefined`.
 *
 * Accepts:
 * - Numbers — truncated with `Math.trunc` if fractional, rejected if negative.
 * - Numeric strings — parsed with `Number()`; rejected if NaN or negative.
 *
 * @param v - The raw candidate value.
 * @returns A non-negative integer, or `undefined` if coercion fails.
 */
function toNonNegativeInt(v: unknown): number | undefined {
    let n: number;
    if (typeof v === 'number') {
        n = Math.trunc(v);
    } else if (typeof v === 'string') {
        const parsed = Number(v.trim());
        if (Number.isNaN(parsed)) {
            return undefined;
        }
        n = Math.trunc(parsed);
    } else {
        return undefined;
    }
    return n >= 0 ? n : undefined;
}

/**
 * Coerces an unknown value to a non-negative number (float allowed), or
 * returns `undefined`.
 *
 * Accepts numbers and numeric strings. Rejects negative values.
 *
 * @param v - The raw candidate value.
 * @returns A non-negative number, or `undefined` if coercion fails.
 */
function toNonNegativeNumber(v: unknown): number | undefined {
    let n: number;
    if (typeof v === 'number') {
        n = v;
    } else if (typeof v === 'string') {
        const parsed = Number(v.trim());
        if (Number.isNaN(parsed)) {
            return undefined;
        }
        n = parsed;
    } else {
        return undefined;
    }
    return n >= 0 ? n : undefined;
}

/**
 * Coerces an unknown value to a WGS-84 coordinate pair `{ lat, long }` where
 * both members are strings (matching `CoordinatesSchema`).
 *
 * Accepts:
 * - An object with numeric or string `lat` / `long` (or `lng` / `longitude`) properties.
 * - An array of two numbers `[lat, long]`.
 *
 * @param v - The raw candidate value.
 * @returns `{ lat: string; long: string }` or `undefined` if coercion fails.
 */
function toCoordinates(v: unknown): { lat: string; long: string } | undefined {
    if (v !== null && typeof v === 'object' && !Array.isArray(v)) {
        const obj = v as Record<string, unknown>;
        const rawLat = obj.lat ?? obj.latitude;
        const rawLong = obj.long ?? obj.lng ?? obj.longitude;

        if (rawLat === undefined || rawLong === undefined) {
            return undefined;
        }

        const lat = typeof rawLat === 'number' ? String(rawLat) : toTrimmedString(rawLat);
        const long = typeof rawLong === 'number' ? String(rawLong) : toTrimmedString(rawLong);

        if (lat === undefined || long === undefined) {
            return undefined;
        }
        return { lat, long };
    }

    if (Array.isArray(v) && v.length >= 2) {
        const [rawLat, rawLong] = v;
        if (typeof rawLat === 'number' && typeof rawLong === 'number') {
            return { lat: String(rawLat), long: String(rawLong) };
        }
    }

    return undefined;
}

