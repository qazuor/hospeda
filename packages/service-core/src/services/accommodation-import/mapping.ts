/** Mutable partial of the extraInfo nested object while assembling. */
interface ExtraInfoAccum {
    capacity?: AccommodationImportDraft['extraInfo'] extends infer E | undefined
        ? E extends { capacity?: infer C }
            ? C
            : never
        : never;
    bedrooms?: AccommodationImportDraft['extraInfo'] extends infer E | undefined
        ? E extends { bedrooms?: infer B }
            ? B
            : never
        : never;
    beds?: AccommodationImportDraft['extraInfo'] extends infer E | undefined
        ? E extends { beds?: infer D }
            ? D
            : never
        : never;
    bathrooms?: AccommodationImportDraft['extraInfo'] extends infer E | undefined
        ? E extends { bathrooms?: infer R }
            ? R
            : never
        : never;
}

/** Mutable partial of the location nested object while assembling. */
interface LocationAccum {
    coordinates?: NonNullable<AccommodationImportDraft['location']>['coordinates'];
    street?: NonNullable<AccommodationImportDraft['location']>['street'];
    number?: NonNullable<AccommodationImportDraft['location']>['number'];
}

/** Mutable partial of the price nested object while assembling. */
interface PriceAccum {
    price?: NonNullable<AccommodationImportDraft['price']>['price'];
    currency?: NonNullable<AccommodationImportDraft['price']>['currency'];
}

/** Mutable partial of the contactInfo nested object while assembling. */
interface ContactInfoAccum {
    mobilePhone?: NonNullable<AccommodationImportDraft['contactInfo']>['mobilePhone'];
    website?: NonNullable<AccommodationImportDraft['contactInfo']>['website'];
}

/** Mutable partial of the seo nested object while assembling. */
interface SeoAccum {
    title?: NonNullable<AccommodationImportDraft['seo']>['title'];
    description?: NonNullable<AccommodationImportDraft['seo']>['description'];
}

// ---------------------------------------------------------------------------
// Internal per-field mapping helper
// ---------------------------------------------------------------------------

/**
 * Attempts to map a single string-valued candidate field into an
 * `{ value, confidence, source }` envelope using the given Zod schema.
 *
 * The schema receives the coerced value. If either coercion or validation
 * fails, returns `undefined` (field is dropped).
 *
 * @param candidate   - Raw candidate with unknown value and source tag.
 * @param coercedValue - Pre-coerced value to validate (already type-narrowed by caller).
 * @param schema      - Zod schema for the wrapped `importedField` object.
 * @param sources     - Set to register the source in on success.
 * @returns The validated envelope or `undefined` on failure.
 */
function tryMapField<T>(
    candidate: RawCandidateField,
    coercedValue: T,
    schema: { safeParse: (v: unknown) => { success: true; data: unknown } | { success: false } },
    sources: Set<FieldSource>
): { value: T; confidence: number; source: FieldSource } | undefined {
    const envelope = {
        value: coercedValue,
        confidence: CONFIDENCE_BY_SOURCE[candidate.source],
        source: candidate.source
    };
    const result = schema.safeParse(envelope);
    if (!result.success) {
        return undefined;
    }
    sources.add(candidate.source);
    return envelope;
}

// ---------------------------------------------------------------------------
// mapRawToDraft
// ---------------------------------------------------------------------------

/**
 * Converts a {@link RawExtraction} into a type-safe {@link AccommodationImportDraft}.
 *
 * For each candidate field in `raw`:
 * 1. Coerces the loose `unknown` value to the target type.
 * 2. Validates the coerced value against the corresponding Zod field schema
 *    (reached via `AccommodationImportDraftSchema.shape`).
 * 3. On validation success, wraps the value as `{ value, confidence, source }`.
 * 4. On coercion or validation failure, silently drops the field.
 *
 * After assembling all passing fields, a final `AccommodationImportDraftSchema.safeParse`
 * guard is run. Any field that still fails at this stage (which should not
 * happen in practice) is stripped by Zod's default `.strip()` behaviour, so
 * the returned draft is always a valid `AccommodationImportDraft`.
 *
 * **Reviews and ratings are never mapped** — they are excluded from
 * `RawExtraction` by design (SPEC-222).
 *
 * @param input - Object containing the `RawExtraction` to map.
 * @returns An object with:
 *   - `draft`       — The valid, partially-populated import draft.
 *   - `methodsUsed` — Distinct list of {@link FieldSource} values that
 *                     contributed at least one field to the draft.
 *
 * @example
 * ```ts
 * const { draft, methodsUsed } = mapRawToDraft({
 *   raw: {
 *     sourcePlatform: 'airbnb',
 *     name: { value: 'Cabaña del Río', source: 'jsonld' },
 *     extraInfo: {
 *       capacity: { value: '6', source: 'text' },
 *     },
 *   },
 * });
 * // draft.name = { value: 'Cabaña del Río', confidence: 90, source: 'jsonld' }
 * // draft.extraInfo.capacity = { value: 6, confidence: 50, source: 'text' }
 * // methodsUsed = ['jsonld', 'text']
 * ```
 */
export const mapRawToDraft = (input: { readonly raw: RawExtraction }): {
    draft: AccommodationImportDraft;
    methodsUsed: FieldSource[];
} => {
    const { raw } = input;

    // Shortcut to the inner value-schema for each top-level importedField.
    const shape = AccommodationImportDraftSchema.shape;

    // Track which FieldSource values contributed at least one field.
    const usedSources = new Set<FieldSource>();

    // Accumulate the assembled draft as a typed partial before the final guard.
    const assembled: Partial<AccommodationImportDraft> = {};

    // -----------------------------------------------------------------------
    // name
    // -----------------------------------------------------------------------
    if (raw.name !== undefined) {
        const coerced = toTrimmedString(raw.name.value);
        if (coerced !== undefined) {
            const mapped = tryMapField(raw.name, coerced, shape.name.unwrap(), usedSources);
            if (mapped !== undefined) {
                assembled.name = mapped;
            }
        }
    }

    // -----------------------------------------------------------------------
    // summary
    // -----------------------------------------------------------------------
    if (raw.summary !== undefined) {
        const coerced = toTrimmedString(raw.summary.value);
        if (coerced !== undefined) {
            const mapped = tryMapField(raw.summary, coerced, shape.summary.unwrap(), usedSources);
            if (mapped !== undefined) {
                assembled.summary = mapped;
            }
        }
    }

    // -----------------------------------------------------------------------
    // description
    // -----------------------------------------------------------------------
    if (raw.description !== undefined) {
        const coerced = toTrimmedString(raw.description.value);
        if (coerced !== undefined) {
            const mapped = tryMapField(
                raw.description,
                coerced,
                shape.description.unwrap(),
                usedSources
            );
            if (mapped !== undefined) {
                assembled.description = mapped;
            }
        }
    }

    // -----------------------------------------------------------------------
    // type
    // -----------------------------------------------------------------------
    if (raw.type !== undefined) {
        const coerced = toTrimmedString(raw.type.value);
        if (coerced !== undefined) {
            const mapped = tryMapField(raw.type, coerced, shape.type.unwrap(), usedSources);
            if (mapped !== undefined) {
                // The value is validated by AccommodationTypeEnumSchema inside the
                // importedField; casting is safe because safeParse confirmed it.
                assembled.type = mapped as AccommodationImportDraft['type'];
            }
        }
    }

    // -----------------------------------------------------------------------
    // extraInfo
    // -----------------------------------------------------------------------
    if (raw.extraInfo !== undefined) {
        const extraInfoShape = shape.extraInfo.unwrap().shape;
        const extraInfoAccum: ExtraInfoAccum = {};

        if (raw.extraInfo.capacity !== undefined) {
            const coerced = toNonNegativeInt(raw.extraInfo.capacity.value);
            if (coerced !== undefined) {
                const mapped = tryMapField(
                    raw.extraInfo.capacity,
                    coerced,
                    extraInfoShape.capacity.unwrap(),
                    usedSources
                );
                if (mapped !== undefined) {
                    extraInfoAccum.capacity = mapped;
                }
            }
        }

        if (raw.extraInfo.bedrooms !== undefined) {
            const coerced = toNonNegativeInt(raw.extraInfo.bedrooms.value);
            if (coerced !== undefined) {
                const mapped = tryMapField(
                    raw.extraInfo.bedrooms,
                    coerced,
                    extraInfoShape.bedrooms.unwrap(),
                    usedSources
                );
                if (mapped !== undefined) {
                    extraInfoAccum.bedrooms = mapped;
                }
            }
        }

        if (raw.extraInfo.beds !== undefined) {
            const coerced = toNonNegativeInt(raw.extraInfo.beds.value);
            if (coerced !== undefined) {
                const mapped = tryMapField(
                    raw.extraInfo.beds,
                    coerced,
                    extraInfoShape.beds.unwrap(),
                    usedSources
                );
                if (mapped !== undefined) {
                    extraInfoAccum.beds = mapped;
                }
            }
        }

        if (raw.extraInfo.bathrooms !== undefined) {
            const coerced = toNonNegativeInt(raw.extraInfo.bathrooms.value);
            if (coerced !== undefined) {
                const mapped = tryMapField(
                    raw.extraInfo.bathrooms,
                    coerced,
                    extraInfoShape.bathrooms.unwrap(),
                    usedSources
                );
                if (mapped !== undefined) {
                    extraInfoAccum.bathrooms = mapped;
                }
            }
        }

        if (Object.keys(extraInfoAccum).length > 0) {
            assembled.extraInfo = extraInfoAccum;
        }
    }

    // -----------------------------------------------------------------------
    // location
    // -----------------------------------------------------------------------
    if (raw.location !== undefined) {
        const locationShape = shape.location.unwrap().shape;
        const locationAccum: LocationAccum = {};

        if (raw.location.coordinates !== undefined) {
            const coerced = toCoordinates(raw.location.coordinates.value);
            if (coerced !== undefined) {
                const mapped = tryMapField(
                    raw.location.coordinates,
                    coerced,
                    locationShape.coordinates.unwrap(),
                    usedSources
                );
                if (mapped !== undefined) {
                    locationAccum.coordinates = mapped;
                }
            }
        }

        if (raw.location.street !== undefined) {
            const coerced = toTrimmedString(raw.location.street.value);
            if (coerced !== undefined) {
                const mapped = tryMapField(
                    raw.location.street,
                    coerced,
                    locationShape.street.unwrap(),
                    usedSources
                );
                if (mapped !== undefined) {
                    locationAccum.street = mapped;
                }
            }
        }

        if (raw.location.number !== undefined) {
            const coerced = toTrimmedString(raw.location.number.value);
            if (coerced !== undefined) {
                const mapped = tryMapField(
                    raw.location.number,
                    coerced,
                    locationShape.number.unwrap(),
                    usedSources
                );
                if (mapped !== undefined) {
                    locationAccum.number = mapped;
                }
            }
        }

        if (Object.keys(locationAccum).length > 0) {
            assembled.location = locationAccum;
        }
    }

    // -----------------------------------------------------------------------
    // price
    // -----------------------------------------------------------------------
    if (raw.price !== undefined) {
        const priceShape = shape.price.unwrap().shape;
        const priceAccum: PriceAccum = {};

        if (raw.price.price !== undefined) {
            const coerced = toNonNegativeNumber(raw.price.price.value);
            if (coerced !== undefined) {
                const mapped = tryMapField(
                    raw.price.price,
                    coerced,
                    priceShape.price.unwrap(),
                    usedSources
                );
                if (mapped !== undefined) {
                    priceAccum.price = mapped;
                }
            }
        }

        if (raw.price.currency !== undefined) {
            const coerced = toTrimmedString(raw.price.currency.value);
            if (coerced !== undefined) {
                const mapped = tryMapField(
                    raw.price.currency,
                    coerced,
                    priceShape.currency.unwrap(),
                    usedSources
                );
                if (mapped !== undefined) {
                    priceAccum.currency = mapped;
                }
            }
        }

        if (Object.keys(priceAccum).length > 0) {
            assembled.price = priceAccum;
        }
    }

    // -----------------------------------------------------------------------
    // contactInfo
    // -----------------------------------------------------------------------
    if (raw.contactInfo !== undefined) {
        const contactShape = shape.contactInfo.unwrap().shape;
        const contactAccum: ContactInfoAccum = {};

        if (raw.contactInfo.mobilePhone !== undefined) {
            const coerced = toTrimmedString(raw.contactInfo.mobilePhone.value);
            if (coerced !== undefined) {
                const mapped = tryMapField(
                    raw.contactInfo.mobilePhone,
                    coerced,
                    contactShape.mobilePhone.unwrap(),
                    usedSources
                );
                if (mapped !== undefined) {
                    contactAccum.mobilePhone = mapped;
                }
            }
        }

        if (raw.contactInfo.website !== undefined) {
            const coerced = toTrimmedString(raw.contactInfo.website.value);
            if (coerced !== undefined) {
                const mapped = tryMapField(
                    raw.contactInfo.website,
                    coerced,
                    contactShape.website.unwrap(),
                    usedSources
                );
                if (mapped !== undefined) {
                    contactAccum.website = mapped;
                }
            }
        }

        if (Object.keys(contactAccum).length > 0) {
            assembled.contactInfo = contactAccum;
        }
    }

    // -----------------------------------------------------------------------
    // seo
    // -----------------------------------------------------------------------
    if (raw.seo !== undefined) {
        const seoShape = shape.seo.unwrap().shape;
        const seoAccum: SeoAccum = {};

        if (raw.seo.title !== undefined) {
            const coerced = toTrimmedString(raw.seo.title.value);
            if (coerced !== undefined) {
                const mapped = tryMapField(
                    raw.seo.title,
                    coerced,
                    seoShape.title.unwrap(),
                    usedSources
                );
                if (mapped !== undefined) {
                    seoAccum.title = mapped;
                }
            }
        }

        if (raw.seo.description !== undefined) {
            const coerced = toTrimmedString(raw.seo.description.value);
            if (coerced !== undefined) {
                const mapped = tryMapField(
                    raw.seo.description,
                    coerced,
                    seoShape.description.unwrap(),
                    usedSources
                );
                if (mapped !== undefined) {
                    seoAccum.description = mapped;
                }
            }
        }

        if (Object.keys(seoAccum).length > 0) {
            assembled.seo = seoAccum;
        }
    }

    // -----------------------------------------------------------------------
    // Final safeParse guard
    // -----------------------------------------------------------------------
    // Run the whole assembled object through the top-level schema. Any field
    // that still fails at this stage (e.g., an upstream schema change) is
    // stripped by Zod's default `.strip()` behaviour, so we always return a
    // valid draft.
    const finalResult = AccommodationImportDraftSchema.safeParse(assembled);

    // In the unlikely event that the entire object fails (e.g., a malformed
    // nested structure), fall back to an empty draft rather than throwing.
    const draft: AccommodationImportDraft = finalResult.success ? finalResult.data : {};

    const methodsUsed = Array.from(usedSources);

    return { draft, methodsUsed };
};
